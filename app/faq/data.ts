/**
 * FAQ型の定義
 *
 * 各FAQは質問（question）と回答（answer）のペアで構成されます。
 *
 * フィールド:
 * - question: 質問文（ユーザーが知りたいこと）
 * - answer: 回答文（店舗側の回答、ポリシー、説明）
 *
 * なぜこの形にしたのか:
 * - シンプルな構造で管理しやすい
 * - 将来的にカテゴリーやタグを追加しやすい
 * - アコーディオンUIに最適な形（質問=タイトル、回答=本文）
 */
export interface FAQ {
  /** 質問文（例: 「かき氷の販売は夏だけですか？」） */
  question: string;
  /** 回答文（例: 「通年で営業しており、季節ごとに異なるメニューもご用意しています。」） */
  answer: string;
}

